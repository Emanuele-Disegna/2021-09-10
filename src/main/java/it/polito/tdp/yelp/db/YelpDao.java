	public void getVertici(String citta, Map<String, Business> idMap){
		String sql = "SELECT DISTINCT * "
				+ "FROM business "
				+ "WHERE city=?";
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, citta);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Business business = new Business(res.getString("business_id"), 
						res.getString("full_address"),
						res.getString("active"),
						res.getString("categories"),
						res.getString("city"),
						res.getInt("review_count"),
						res.getString("business_name"),
						res.getString("neighborhoods"),
						res.getDouble("latitude"),
						res.getDouble("longitude"),
						res.getString("state"),
						res.getDouble("stars"));
				idMap.put(res.getString("business_id")  ,business);
			}
			res.close();
			st.close();
			conn.close();
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}
	
	public List<Adiacenza> getArchi(String citta, Map<String, Business> idMap){
		String sql = "SELECT DISTINCT b1.business_id AS b1, b2.business_id AS b2, "
				+ "b1.latitude AS la1, b1.longitude AS lo1,b2.latitude AS la2, b2.longitude AS lo2 "
				+ "FROM business b1, business b2 "
				+ "WHERE b1.city=? AND b1.city=b2.city AND b1.business_id>b2.business_id "
				+ "GROUP BY b1.business_id, b2.business_id";
		List<Adiacenza> result = new ArrayList<Adiacenza>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, citta);
			ResultSet res = st.executeQuery();
			while (res.next()) {
				Adiacenza a = new Adiacenza(idMap.get(res.getString("b1")),
							idMap.get(res.getString("b2")),
							new LatLng(res.getDouble("la1"), res.getDouble("lo1")),
							new LatLng(res.getDouble("la2"), res.getDouble("lo2")));
				result.add(a);
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	public List<String> getCitta(){
		String sql = "SELECT DISTINCT city "
				+ "FROM business ";
		List<String> result = new ArrayList<>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			ResultSet res = st.executeQuery();
			while (res.next()) {
				result.add(res.getString("city"));
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
